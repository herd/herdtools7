inline static tb_t timeofday(void) {
  uint32_t a,d; ;
  asm __volatile__ ("rdtsc" : "=a" (a), "=d" (d)) ;
  tb_t r = ((tb_t)a) | (((tb_t)d)<<32);
  return r ;
}
