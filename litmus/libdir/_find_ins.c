// Find index of some instruction in code, skipping 'skip' occurrences
static size_t find_ins(ins_t opcode,ins_t *p,int skip) {
  ins_t *q = p;

  for  ( ; *q != opcode || (skip-- > 0); q++);
  return q-p ;
}
