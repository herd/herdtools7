type t = Only | Yes | No

let tags = ["yes";"no";"only";]

let parse tag  = match Misc.lowercase tag with
| "only" -> Some Only
| "yes" -> Some Yes
| "no" -> Some No
| _ -> None

let pp = function
  | Only -> "only"
  | Yes -> "yes"
  | No -> "no"
